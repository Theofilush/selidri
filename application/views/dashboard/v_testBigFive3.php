                                <h3 class="mt-0 ">Tentang Big Five (OCEAN) Personality Test</h3>
                                <div class="row">
                                    <div class="col-lg-12">
                                        <div style="text-align:center;margin: 20px;">
                                            <img src="<?php echo base_url() ?>assets/images/ocean1.png" alt="holland1" style="height:20vw;">
