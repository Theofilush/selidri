                                        <p class="font-14">Hai, Perseners! Kenalin aku Nida, Associate Writer Satu Persen.</p>
                                        <p class="font-14">Nah, di artikel sebelumnya aku ada ngebahas tentang tes psikologi yang bernama RIASEC. Perseners udah baca belum? Kalau belum, bisa baca dulu nih artikel aku di sini.</p>
                                        <p class="font-14">Di artikel itu, aku ngebahas apa sih RIASEC Test itu? Gimana perkembangan tesnya? dan tujuan dari tesnya itu apa?</p>
                                        <p class="font-14">Kalau kamu belum baca, aku akan bahas sedikit disini, buat ngebantu kamu mengingat atau mengetahui secara singkat tentang tes ini.</p>
                                        <p class="font-14">Jadi, RIASEC Test merupakan tes psikologi yang dapat kamu gunakan untuk mengenal diri, serta membantu memperkirakan karier apa yang cocok untuk diri kamu. Tes ini dikembangkan berdasarkan teori kepribadian dari John Holland, seorang Psikolog di Amerika.</p>
                                        <p class="font-14">Menurut Holland, setiap orang memiliki satu tipe kepribadian yang dominan, tipe kepribadiannya ini dapat memperkirakan pekerjaan apa yang cocok untuknya, serta lingkungan kerja seperti apa yang membuatnya nyaman.</p>
                                        <p class="font-14">Nah, tes ini bisa sangat membantu ketika kamu masih merasa bingung dengan passion atau karier seperti apa yang cocok buat kamu.</p>
                                        <p class="font-14">Oke, di artikel ini kita kenalan sama enam tipe kepribadian yang ada di Tes RIASEC ini yuk! Akan ada satu, dua, atau tiga kepribadian yang cenderung menggambarkan dirimu, tapi di antara ketiga tipe itu, cuma ada satu nih, yang dominan.</p>

                                        <h2>Enam Tipe Kepribadian versi Holland (RIASEC)</h2>
                                        
                                        <h5>Realistic</h5>
                                        <p class="font-14">Tipe yang pertama ini memiliki kecenderungan berorientasi pada penerapan yang teratur dan sistematis. Kamu menyukai tugas-tugas yang konkrit, praktis, mengutamakan keterampilan fisik, dan kekuatan otot.</p>
                                        <p class="font-14">Tapi, kamu memiliki keterampilan sosial yang kurang baik, dan kurang peka dengan lingkungan sekitar atau orang lain, sehingga pekerjaan yang berkaitan dengan orang lain akan kamu hindari.</p>
                                        <p class="font-14">Contoh pekerjaan yang cocok pada tipe ini adalah operator mesin, ahli kelistrikan, dan pekerjaan lain yang sejenis.</p>
                                        
                                        <h5>Investigative</h5>
                                        <p class="font-14">Pada tipe ini kamu memiliki kecenderungan untuk memilih pekerjaan yang bersifat akademik dan intelektual. Kamu akan menyukai tugas-tugas yang abstrak, membutuhkan inteligensi dan kreativitas yang tinggi.</p>
                                        <p class="font-14">Kriteria keberhasilan pekerjaan buat kamu adalah bersifat objektif dan dapat diukur. Sehingga tipe ini juga dikenal sebagai “Pemikir”.</p>
                                        <p class="font-14">Contoh pekerjaannya adalah ahli fisika, ahli biologi, antropologi, pekerjaan penelitian, dan pekerjaan lain yang sejenis.</p>
                                        
                                        <h5>Artistic</h5>
                                        <p class="font-14">Tipe kepribadian ini memiliki kecenderungan untuk menjalin hubungan dengan orang lain secara gak langsung, dan juga mengalami kesulitan untuk menyesuaikan diri. Kamu akan menyukai tugas-tugas yang berkaitan dengan hal-hal yang membutuhkan interpretasi atau kreasi melalui perasaan dan imajinasi.</p>
                                        <p class="font-14">Kamu bisa sangat menghindari pekerjaan yang menuntut keteraturan, dan menjalin hubungan atau bersosial.</p>
                                        <p class="font-14">Jadi, contoh pekerjaan yang sesuai dengan tipe ini adalah pemusik, pencipta lagu, pelukis, dan pekerjaan lain yang sejenis.</p>
                                        
                                        <h5>Social</h5>
                                        <p class="font-14">Sesuai dengan namanya, tipe kepribadian yang satu ini dikenal sebagai “Helpers”. Tipe ini memiliki kecenderungan untuk membantu dan mementingkan orang lain. Kamu juga menyukai membangun hubungan dengan banyak orang, dan berkomunikasi merupakan kelebihan yang kamu miliki.</p>
                                        <p class="font-14">Tipe pekerjaan yang kamu sukai tentunya pekerjaan yang berhubungan langsung dengan orang lain, dan kegiatan kemanusiaan.</p>
                                        <p class="font-14">Contoh pekerjaan yang sesuai dengan tipe ini adalah konselor, guru, pekerja sosial, dan pekerjaan lain yang sejenis.</p>
                                        
                                        <h5>Enterprising</h5>
                                        <p class="font-14">Tipe kepribadian ini adalah tipe yang paling menyukai kegiatan membujuk orang lain. Kamu paling jago untuk mengajak, menawarkan, dan merayu lawan bicaramu. Kelebihanmu adalah kamu mampu menyampaikan sesuatu secara efektif dan menarik, hingga dapat memengaruhi orang lain.</p>
                                        <p class="font-14">Tipe pekerjaan yang sesuai dengan kepribadian ini adalah pekerjaan yang berkaitan dengan kegiatan yang menggunakan kemampuan verbal untuk mengarahkan dan mempengaruhi orang lain. Contoh pekerjaan yang sesuai dengan tipe ini adalah penjual, politikus, manajer perusahaan,, dan pekerjaan lain yang sejenis.</p>
                                        
                                        <h5>Conventional</h5>
                                        <p class="font-14">Tipe kepribadian yang terakhir cenderung menyukai kegiatan yang teratur dan terorganisir. Kamu menyukai menjalani sehari-hari dengan rutinitas yang jelas, dengan jadwal-jadwal yang tersusun rapi.</p>
                                        <p class="font-14">Kamu akan menghindari situasi-situasi yang abstrak, karena biasanya dalam situasi tersebut kamu akan merasa gak nyaman.</p>
                                        <p class="font-14">Tipe pekerjaan yang cocok untuk kamu adalah pekerjaan yang membutuhkan ketelitian, terorganisir dengan baik, dan bersifat terorganisir. Contoh pekerjaan yang sesuai untuk tipe ini adalah pegawai kantor, penjaga kasir, statistika, pegawai bank, dan pekerjaan lain yang sejenis.</p>

